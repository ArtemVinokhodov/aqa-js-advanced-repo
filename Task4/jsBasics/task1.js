function declarePrimitives() {
	let aString = 'Hello';
	let aNumber = 10;
	let aBoolean = true;
	let aNull = null;
	let anUndefined;

	console.log(`String: ${aString}`);
	console.log(`Number: ${aNumber}`);
	console.log(`Boolean: ${aBoolean}`);
	console.log(`Null value: ${aNull}`);
	console.log(`Undefined value: ${anUndefined}`);
}

declarePrimitives();
